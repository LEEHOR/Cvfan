package com.coahr.cvfan.entity;

public class AppraiseStationEntity {
    private String driverId;
    private String stationId;
    private String generalScore;
    private String priceScore;
    private String qualityScore;
    private String timeScore;
    private String comment;
    public String getDriverId() {
        return driverId;
    }
    public void setDriverId(String driverId) {
        this.driverId = driverId;
    }
    public String getStationId() {
        return stationId;
    }
    public void setStationId(String stationId) {
        this.stationId = stationId;
    }
    public String getGeneralScore() {
        return generalScore;
    }
    public void setGeneralScore(String generalScore) {
        this.generalScore = generalScore;
    }
    public String getPriceScore() {
        return priceScore;
    }
    public void setPriceScore(String priceScore) {
        this.priceScore = priceScore;
    }
    public String getQualityScore() {
        return qualityScore;
    }
    public void setQualityScore(String qualityScore) {
        this.qualityScore = qualityScore;
    }
    public String getTimeScore() {
        return timeScore;
    }
    public void setTimeScore(String timeScore) {
        this.timeScore = timeScore;
    }
    public String getComment() {
        return comment;
    }
    public void setComment(String comment) {
        this.comment = comment;
    }
        
    
}
